#!/usr/bin/env python3

num_1 = int(input("Me dê o primeiro número: "))
num_2 = int(input("Me dê o segundo número: "))

print("Obrigado!")

print(f"{num_1} + {num_2} = {num_1 + num_2}")
print(f"{num_1} - {num_2} = {num_1 - num_2}")
print(f"{num_1} / {num_2} = {int(num_1 / num_2)}")
print(f"{num_1} * {num_2} = {num_1 * num_2}")

